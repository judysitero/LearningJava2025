package com.pluralsight;

public class OperatorExercises {
    public static void main(String[] args) {
        //COUNTING COINS:
        int a = 137;
        int b = 20;
        int result;
        result = a / b;
        System.out.println(result);
// player can fill 6 bags full

        result = a % b;
        System.out.println(result);
// with a remainder of 17 coins left over


//      POTION MIXING:
        int redHerb = 17;
        int redHerb2 = 3;
        result = redHerb / redHerb2;
        System.out.println(result);
//  they can make 5 red potions

        int blueHerb = 11;
        int blueHerb2 = 2;
        result = blueHerb / blueHerb2;

        System.out.println(result);

    }
}
